//A list of constants used to determine which key corresponds to which action in the program.

#ifndef _CCONSTANTS_H_
#define _CCONSTANTS_H_

#define KEY_FORWARD VK_UP
#define KEY_BACKWARD VK_DOWN
#define KEY_LEFT VK_LEFT
#define KEY_RIGHT VK_RIGHT
#define KEY_UP VK_HOME
#define KEY_DOWN VK_END
#define KEY_RESET VK_SPACE
#define KEY_GODMODE VK_RETURN
#define KEY_NORMALISE 0x4E					//Code for N key
#define KEY_GENERATE_RANDOM 0x47			//Code for G key
#define KEY_RAISE_SEALEVEL VK_PRIOR			//Code for Page Up
#define KEY_LOWER_SEALEVEL VK_NEXT			//Code for Page Down
#define KEY_SMOOTH_AVERAGE 0x31				//Code for number 1 key
#define KEY_MIDPOINT_DISPLACEMENT 0x32		//Code for number 2 key
#define KEY_FAULTING 0x33					//Code for number 3 key
#define KEY_PARTICLE_DEPOSITION_STICKY 0x34	//Code for number 4 key
#define KEY_PARTICLE_DEPOSITION 0x35		//Code for number 5 key

#endif //_CCONSTANTS_H_